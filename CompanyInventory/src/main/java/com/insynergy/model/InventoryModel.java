package com.insynergy.model;

public class InventoryModel {
	
	private String inventoryName;
	private String inventoryModel;
	private int inventoryCount;
	private String inventorySerialNo;
	private String inventoryDate;
	public String getInventoryDate() {
		return inventoryDate;
	}
	public void setInventoryDate(String inventoryDate) {
		this.inventoryDate = inventoryDate;
	}
	private double inventoryPrice;
	private String inventoryRemarks;
	private String inventoryMake;
	public String getInventoryName() {
		return inventoryName;
	}
	public void setInventoryName(String inventoryName) {
		this.inventoryName = inventoryName;
	}
	public String getInventoryModel() {
		return inventoryModel;
	}
	public void setInventoryModel(String inventoryModel) {
		this.inventoryModel = inventoryModel;
	}
	public int getInventoryCount() {
		return inventoryCount;
	}
	public void setInventoryCount(int inventoryCount) {
		this.inventoryCount = inventoryCount;
	}
	public String getInventorySerialNo() {
		return inventorySerialNo;
	}
	public void setInventorySerialNo(String inventorySerialNo) {
		this.inventorySerialNo = inventorySerialNo;
	}
	public double getInventoryPrice() {
		return inventoryPrice;
	}
	public void setInventoryPrice(double inventoryPrice) {
		this.inventoryPrice = inventoryPrice;
	}
	public String getInventoryRemarks() {
		return inventoryRemarks;
	}
	public void setInventoryRemarks(String inventoryRemarks) {
		this.inventoryRemarks = inventoryRemarks;
	}
	public String getInventoryMake() {
		return inventoryMake;
	}
	public void setInventoryMake(String inventoryMake) {
		this.inventoryMake = inventoryMake;
	}
	
	

}
